def divide_by_2(num):
    """
    Returns the value divided by 2
    :param num: int.
    :return: int.
    """
    return num // 2

def multiply_4(num):
    """
    Returns the value multiplied by 4
    :param num: int.
    :return: int.
    """
    return num * 4

x = divide_by_2(4)
print(multiply_4(x))